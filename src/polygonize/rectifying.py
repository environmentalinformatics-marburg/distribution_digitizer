# ------------------------------------------------------------
# Rectifying workflow for Point Filtering (multi map-type version)
# ------------------------------------------------------------
# Purpose:
# Applies rectification to multiple map types (e.g., 1, 2, …),
# each processed independently in the pipeline.
#
# Key concept:
# The Distribution Digitizer supports multiple map groups
# (e.g., different symbol systems or datasets).
#
# Workflow:
# For each map type:
# 1. Locate input directory:
#    <outDir>/<map_type>/georeferencing/masks/pointFiltering/
# 2. Locate output directory:
#    <outDir>/<map_type>/rectifying/pointFiltering/
# 3. Process all TIFF files
# 4. Apply rectification
#
# Robustness:
# - Skips map types without data
# - Provides debug output for traceability
#
# Importance:
# - Ensures consistent geometry across all map types
# - Enables scalable processing of large datasets
#
# Design advantage:
# - Modular structure allows independent processing per map type
# - Facilitates parallelization and debugging
# ------------------------------------------------------------
def mainRectifying_PF(workingDir, outDir, nMapTypes=1):

    print(f"DEBUG Rectifying nMapTypes = {nMapTypes}")

    for i in range(1, nMapTypes + 1):

        print(f"\n=== Rectifying map type {i} ===")

        inputdir = os.path.join(
            outDir, str(i), "georeferencing", "masks", "pointFiltering"
        )

        output = os.path.join(
            outDir, str(i), "rectifying", "pointFiltering"
        )

        os.makedirs(output, exist_ok=True)

        print("Input directory:", inputdir)
        print("Output directory:", output)

        tif_files = glob.glob(os.path.join(inputdir, "*.tif"))

        if not tif_files:
            print("⚠️ No tif files found for rectifying")
            continue

        for input_raster in tif_files:
            print("Rectifying:", input_raster)

            dst_layername = os.path.basename(input_raster)
            output_raster = os.path.join(output, dst_layername)

            rectifying(input_raster, output_raster)

