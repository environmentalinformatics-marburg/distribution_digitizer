# ------------------------------------------------------------
# Main workflow for rectifying maps (Point Filtering version)
# ------------------------------------------------------------
# Purpose:
# Applies rectification to all georeferenced map images that were
# generated from Point Filtering results.
#
# Input:
# - Georeferenced raster maps containing detected centroids
#
# Output:
# - Rectified raster maps with corrected geometry
#
# Processing steps:
# 1. Locate input directory:
#    georeferencing/maps/pointFiltering/
# 2. Iterate over all TIFF files
# 3. Apply rectification to each file
# 4. Save results in:
#    rectifying/maps/
#
# Importance:
# - Ensures maps are geometrically stable before spatial analysis
# - Removes distortions introduced during georeferencing
#
# Design decision:
# - Batch processing per directory for efficiency
# - File names are preserved for traceability
# ------------------------------------------------------------
def mainRectifying_Map_PF(workingDir, outDir):
    try:
        output = os.path.join(outDir, "rectifying", "maps")
        os.makedirs(output, exist_ok=True) 
        inputdir = os.path.join(outDir, "georeferencing", "maps", "pointFiltering")
        print("Input Directory:", inputdir)
        
        # Adjusted glob.glob to correctly capture all TIFF files
        for input_raster in glob.glob(inputdir + "/*.tif"):
            print("Input TIFF:", input_raster)
            dst_layername = os.path.basename(input_raster)
            print("Destination Layer Name:", dst_layername)
            output_raster = os.path.join(output, dst_layername)
            print("Output Raster Path:", output_raster)
            # Assuming rectifying() is a function you have defined elsewhere
            rectifying(input_raster, output_raster)
    
    except Exception as e:
        print("An error occurred in mainRectifying_Map_PF:", e)
    # End of function


# ------------------------------------------------------------
# Main workflow for rectifying masked centroid images
# ------------------------------------------------------------
# Purpose:
# Rectifies raster images containing centroid masks after
# georeferencing.
#
# Input:
# - Masked raster images (binary or colored centroid layers)
#
# Output:
# - Rectified mask images with corrected geometry
#
# Key idea:
# Even binary masks can be geometrically distorted after
# georeferencing → rectification is required to:
# - align masks with real-world coordinates
# - ensure correct spatial overlay
#
# Processing:
# - Iterate over all mask TIFF files
# - Apply rectifying() function
# - Save outputs in rectifying/ directory
#
# Notes:
# - Essential for accurate polygon generation
# - Prevents spatial misalignment in later steps
# ------------------------------------------------------------
def mainRectifying(workingDir, outDir):
  try:
    output= outDir + "/rectifying/"
    os.makedirs(output, exist_ok=True) 
    inputdir = outDir +"/georeferencing/masks/"
    print(inputdir)
    for input_raster in glob.glob(inputdir + "*.tif"):
      print(input_raster)
      dst_layername = os.path.basename(input_raster)
      print(dst_layername)
      output_raster = output + dst_layername
      print(output_raster)
      rectifying(input_raster, output_raster)
  except Exception as e:
        print("An error occurred in mainRectifying:", e)
  # End of function


