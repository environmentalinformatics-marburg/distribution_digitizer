#### Script for iteratively rectifying the georeferenced output GeoTIFF files from '5. Georeferencing'.

import sys

import rasterio
from osgeo import gdal, osr
import os, glob


# ------------------------------------------------------------
# Core rectification function
# ------------------------------------------------------------
# Purpose:
# This function performs geometric rectification of already
# georeferenced raster images using GDAL.
#
# Background:
# After georeferencing (based on GCPs), raster images may still
# contain distortions such as:
# - curved or warped edges
# - non-rectangular boundaries
# - irregular pixel alignment
#
# These artefacts are typical results of transformation models
# applied during georeferencing.
#
# Method:
# - The input raster is opened using GDAL
# - gdal.Warp() is applied to reproject and resample the image
# - The result is written as a new GeoTIFF with corrected geometry
#
# Result:
# - Straightened image borders
# - Improved geometric consistency
# - Cleaner input for downstream processing (e.g., polygonization)
#
# Notes:
# - This step does NOT change content, only geometry
# - It ensures that raster data aligns properly with spatial coordinates
# ------------------------------------------------------------
def rectifying(input_raster, output_raster):
    # Öffnen des Quell-Datasets
    src_ds = gdal.Open(input_raster)
    if src_ds is None:
        print(f"Fehler beim Öffnen der Eingabedatei: {input_raster}")
        return
    
    # Bestimmen des Ausgabepfads
    output_raster, file_extension = os.path.splitext(output_raster)
    dst_path = output_raster + ".tif"
    
    # Überprüfen, ob das Zielverzeichnis vorhanden ist, andernfalls erstellen
    output_dir = os.path.dirname(dst_path)
    if not os.path.exists(output_dir):
        os.makedirs(output_dir)
    
    try:
        # Ausführen der Rektifizierung mit gdal.Warp()
        dst_ds = gdal.Warp(dst_path, src_ds)
        dst_ds = None
        src_ds = None
        print(f"Rektifizierte Datei gespeichert: {dst_path}")
    except Exception as e:
        print(f"Fehler bei der Rektifizierung: {e}")

